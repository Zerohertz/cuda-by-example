#include "../../include/handler.cuh"

__global__ void stage1(int *input, int *intermediate, int size)
{
    int tid = blockIdx.x * blockDim.x + threadIdx.x;
    if (tid < size) {
        intermediate[tid] = input[tid] + 1;
    }
}

__global__ void stage2(int *intermediate, int *output, int size)
{
    int tid = blockIdx.x * blockDim.x + threadIdx.x;
    if (tid < size) {
        output[tid] = intermediate[tid] * 2;
    }
}


int main()
{
    const int size = 1000;

    int *host_input  = new int[size];
    int *host_output = new int[size];

    for (int i = 0; i < size; i++) {
        host_input[i] = i;
    }

    int *dev_input, *dev_intermediate, *dev_output;
    CUDA_CHECK(cudaMalloc(&dev_input, size * sizeof(int)));
    CUDA_CHECK(cudaMalloc(&dev_intermediate, size * sizeof(int)));
    CUDA_CHECK(cudaMalloc(&dev_output, size * sizeof(int)));

    cudaStream_t stream1, stream2;
    CUDA_CHECK(cudaStreamCreate(&stream1));
    CUDA_CHECK(cudaStreamCreate(&stream2));

    CUDA_CHECK(cudaMemcpyAsync(dev_input, host_input, size * sizeof(int), cudaMemcpyHostToDevice, stream1));

    dim3 blockDim(256);
    dim3 gridDim((size + blockDim.x - 1) / blockDim.x);
    stage1<<<gridDim, blockDim, 0, stream1>>>(dev_input, dev_intermediate, size);
    CUDA_CHECK(cudaGetLastError());

    CUDA_CHECK(cudaMemcpyPeerAsync(dev_intermediate, 1, dev_intermediate, 0, size * sizeof(int), stream2));

    stage2<<<gridDim, blockDim, 0, stream2>>>(dev_intermediate, dev_output, size);
    CUDA_CHECK(cudaGetLastError());

    CUDA_CHECK(cudaMemcpyAsync(host_output, dev_output, size * sizeof(int), cudaMemcpyDeviceToHost, stream2));

    CUDA_CHECK(cudaStreamSynchronize(stream2));

    LOG_INFO("Result:");
    for (int i = 0; i < size; i++) {
        LOG_INFO(host_output[i]);
    }

    CUDA_CHECK(cudaFree(dev_input));
    CUDA_CHECK(cudaFree(dev_intermediate));
    CUDA_CHECK(cudaFree(dev_output));
    CUDA_CHECK(cudaStreamDestroy(stream1));
    CUDA_CHECK(cudaStreamDestroy(stream2));
    delete[] host_input;
    delete[] host_output;

    return 0;
}

