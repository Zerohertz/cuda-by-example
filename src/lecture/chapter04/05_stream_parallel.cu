#include "../../include/handler.cuh"

__global__ void kernel1(int *data)
{
    int tid = blockIdx.x * blockDim.x + threadIdx.x;
    data[tid] += 1;
}

__global__ void kernel2(int *data)
{
    int tid = blockIdx.x * blockDim.x + threadIdx.x;
    data[tid] *= 2;
}


int main()
{
    int data_size  = 1024;
    int block_size = 256;
    int grid_size  = data_size / block_size;

    int *host_data_1;
    int *host_data_2;
    int *dev_data_1;
    int *dev_data_2;

    host_data_1 = (int *)malloc(data_size * sizeof(int));
    CUDA_CHECK(cudaMalloc(&dev_data_1, data_size * sizeof(int)));

    host_data_2 = (int *)malloc(data_size * sizeof(int));
    CUDA_CHECK(cudaMalloc(&dev_data_2, data_size * sizeof(int)));

    for (int i = 0; i < data_size; i++) {
        host_data_1[i] = i;
        host_data_2[i] = i;
    }

    cudaStream_t stream1, stream2;
    CUDA_CHECK(cudaStreamCreate(&stream1));
    CUDA_CHECK(cudaStreamCreate(&stream2));

    CUDA_CHECK(cudaMemcpyAsync(dev_data_1, host_data_1, data_size * sizeof(int), cudaMemcpyHostToDevice, stream1));
    kernel1<<<grid_size, block_size, 0, stream1>>>(dev_data_1);
    CUDA_CHECK(cudaGetLastError());

    CUDA_CHECK(cudaMemcpyAsync(dev_data_2, host_data_2, data_size * sizeof(int), cudaMemcpyHostToDevice, stream2));
    kernel2<<<grid_size, block_size, 0, stream2>>>(dev_data_2);
    CUDA_CHECK(cudaGetLastError());

    CUDA_CHECK(cudaStreamSynchronize(stream1));
    CUDA_CHECK(cudaStreamSynchronize(stream2));

    LOG_INFO("Data 1:");
    for (int i = 0; i < data_size; i++) {
        LOG_INFO(host_data_1[i]);
    }
    LOG_INFO("Data 2:");
    for (int i = 0; i < data_size; i++) {
        LOG_INFO(host_data_2[i]);
    }

    CUDA_CHECK(cudaStreamDestroy(stream1));
    CUDA_CHECK(cudaStreamDestroy(stream2));
    free(host_data_1);
    CUDA_CHECK(cudaFree(dev_data_1));
    free(host_data_2);
    CUDA_CHECK(cudaFree(dev_data_2));

    return 0;
}

